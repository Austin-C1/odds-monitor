    expect(source).not.toContain('/odds-monitor/crown/accounts/check')
    expect(source).not.toContain('登录密码')
    expect(source).not.toContain('Input.Password')
    expect(source).not.toContain('password: values.password')
    expect(source).toContain('在线')
    expect(source).toContain('账号在线，余额已获取')
    expect(source).toContain('异常')
    expect(source).toContain('自动化接入投注功能')
    expect(source).toContain('赛前')
    expect(source).toContain('滚球')
    expect(source).toContain('自动投注')
    expect(source).toContain('自动监听中')
    expect(source).toContain('自动投注结果')
    expect(source).not.toContain('运行实际测试')
    expect(source).not.toContain('PlayCircleOutlined')
    expect(source).toContain('保存设置')
    expect(source).toContain('crown-betting-automation-settings')
    expect(source).not.toContain('模拟模式')
    expect(source).not.toContain('运行模拟投注')
    expect(source).not.toContain('多账号模拟结果')
    expect(source).not.toContain('模拟赔率')
    expect(source).not.toContain('尚未运行模拟投注')
    expect(source).toContain('每号金额限制')
    expect(source).toContain('投注上限')
    expect(source).not.toContain('赔率波动容忍')
    expect(source).not.toContain('最低赔率优势')
    expect(source).toContain('最低投注水位')
    expect(source).toContain('信号有效期(秒)')
    expect(source).toContain('maxSignalAgeSeconds')
    expect(source).toContain('AdsPower 档案 ID')
    expect(source).toContain('检测 AdsPower')
    expect(source).toContain('打开环境')
    expect(source).toContain('/auto-betting/adspower/status')
    expect(source).toContain('/auto-betting/adspower/start-profile')
    expect(source).toContain('/auto-betting/adspower/crown-session')
    expect(source).toContain('/auto-betting/adspower/crown-session/match')
    expect(source).toContain("import { extractApiErrorMessage } from '../../utils/apiError'")
    expect(source).toContain('extractApiErrorMessage(error,')
    expect(source).toContain('matchAdsPowerCrownSession')
    expect(source).toContain('/auto-betting/signals/odds-monitor/execute-crown-queue')
    expect(source).not.toContain('/auto-betting/intents/${decision.id}/execute-crown')
    expect(source).toContain('crownHistoryVerified')
    expect(source).toContain('crownBetReference')
    expect(source).toContain('已下注')
    expect(source).toContain('后端队列执行')
    expect(source).toContain('下注队列已完成，成功记录已写入后端')
    expect(source).toContain('本轮信号没有确认成功，后续可重新尝试')
    expect(source).not.toContain('下注成功，已核对皇冠投注历史')
    expect(source).toContain('/odds-monitor/alerts/list')
    expect(source).toContain('executeLatestCrownSignal')
    expect(source).not.toContain('attemptedSignalAtRef')
    expect(source).toContain('候选信号盘口')
