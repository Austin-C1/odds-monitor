    expect(source).toContain('队列 #')
    expect(source).toContain('水位变化')
    expect(source).toContain('bettingMode: activeSignal.bettingMode')
    expect(source).toContain('matchPhase: activeSignal.matchPhase')
    expect(source).toContain('accountDisplayName: row.accountName')
    expect(source).toContain("row.matchPhase === 'prematch'")
    expect(source).toContain('未绑定 AdsPower 档案')
    expect(source).toContain("account.adsPowerStatus === 'opened'")
    expect(source).toContain("account.status === 'success'")
    expect(source).toContain('buildAutoBettingExecutionPlan')
    expect(source).toContain('extractCrownAlertSignalCandidates')
    expect(source).toContain('filterFreshCrownAlertSignals')
    expect(source).not.toContain('buildAutoBettingSimulation')
    expect(source).not.toContain('crownBettingSimulation')
    expect(source).not.toContain('阿森纳 vs 切尔西')
    expect(source).not.toContain('国际米兰 vs 尤文图斯')
    expect(source).toContain('删除账号')
    expect(source).toContain('crown-betting-accounts')
    expect(source).toContain('updateAutoBettingEnabled')
    expect(source).toContain('localStorage')
    expect(source).not.toContain('DATA_SOURCE_ACCOUNT_ID')
    expect(source).not.toContain('/odds-monitor/data-sources/configs/list')
